/**
 * The options of the TarModal component.
 */
export type ModalOptions = {
  /**
   * The close label for accessibility.
   */
  close?: string;
  /**
   * The unique identifier of the modal.
   */
  id?: string;
  /**
   * The title that will appear in the toast header.
   */
  title?: string;
};

